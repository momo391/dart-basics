//! 1-dart basics:
// dart is a static typed language
// void main () {} : for running main function.
// var <var name> = <var value> : for creating variable , var will automatically get its type from declaration.
// semicolon is required at the end of the line.
// final instead of var: for creating final variable and value will be initialized at runtime ( don't know the value in compile time ).
// const instead of var: for creating const variable and value will be initialized at compile time ( know the value in compile time ).
// operations is the same as java.
// print() : function for printing variable.
// $<var name>: for string interpolation using variables.
// ${}: for string interpolation using expressions.

